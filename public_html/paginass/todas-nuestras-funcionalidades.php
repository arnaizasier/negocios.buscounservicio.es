    <script>
        document.addEventListener('DOMContentLoaded', function() {
            const tabs = document.querySelectorAll('.feature-tab');
            const allFeatures = document.querySelectorAll('.feature-item');
            
            // Categorías actualizadas para filtrar con los nuevos bloques
            const categories = {
                'Reservas': [0, 1, 6],  // Reserva, Catálogo, Descuentos
                'Productos': [2, 6],    // Venta de productos, Descuentos
                'Comunicación': [4, 7], // Enlaces sociales, Chat
                'Gestión': [9, 10, 11],     // Horario, Finanzas, Clientes
                'Atención al Cliente': [3, 8, 7]  // Reseñas, Preguntas Frecuentes, Chat
            };
            
            tabs.forEach(tab => {
                tab.addEventListener('click', function() {
                    // Quitar clase activa de todas las pestañas
                    tabs.forEach(t => t.classList.remove('active'));
                    
                    // Añadir clase activa a la pestaña seleccionada
                    this.classList.add('active');
                    
                    // Filtrar características según la pestaña
                    const category = this.textContent.trim();
                    
                    if(category === 'Todas') {
                        // Mostrar todas las características
                        allFeatures.forEach(feature => {
                            feature.style.display = 'block';
                            setTimeout(() => feature.style.opacity = 1, 10);
                        });
                    } else {
                        // Mostrar solo las características de la categoría seleccionada
                        allFeatures.forEach((feature, index) => {
                            if(categories[category] && categories[category].includes(index)) {
                                feature.style.display = 'block';
                                setTimeout(() => feature.style.opacity = 1, 10);
                            } else {
                                feature.style.opacity = 0;
                                setTimeout(() => feature.style.display = 'none', 300);
                            }
                        });
                    }
                });
            });
        });
    </script>

